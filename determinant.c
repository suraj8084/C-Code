#include<stdio.h>
#include<conio.h>
void main()
{
    int i,j,m,n;
    float a[5][5],d;
    printf("Enter Order of Second Matrix :");
    scanf("%d %d",&m,&n);
    if(m!=n)
    printf("Determinant is Not Possible.");
    else
    {
    printf("Enter matrix :\n");
        for(i=0;i<m;i++)
        {
            for(j=0;j<n;j++)
            {
                scanf("%f",&a[i][j]);
            }
        }
        if(m==1)
        {
            d = a[0][0];
        }
        else if(m==2)
        {
            d = a[0][0]*a[1][1]-a[0][1]*a[1][0];
        }
        else if (m==3)
        {
            d = a[0][0]*(a[1][1]*a[2][2]-a[1][2]*a[2][1])-a[1][1]*(a[1][0]*a[2][2]-a[1][2]*a[2][0])+a[2][2]*(a[1][0]*a[2][1]-a[2][2]*a[2][0]);
        }
        else if (m==4)
        {
            d = a[0][0]*(a[1][1]*(a[2][2]*a[3][3]-a[2][3]*a[3][2])-a[1][2]*(a[2][1]*a[3][3]-a[2][3]*a[3][1])+a[1][3]*(a[2][1]*a[3][2]-a[2][2]*a[3][1])) -a[0][1]*(a[1][0]*(a[3][3]*a[2][2]-a[2][3]*a[3][2])-a[1][2]*(a[2][0]*a[3][3]-a[2][3]*a[3][0])+a[1][3]*(a[2][0]*a[3][2]-a[2][2]*a[3][0])) +a[0][2]*(a[1][0]*(a[2][1]*a[3][3]-a[2][3]*a[3][1])-a[1][1]*(a[2][0]*a[3][3]-a[2][3]*a[3][0])+a[1][3]*(a[2][0]*a[3][2]-a[2][1]*a[3][0])) -a[0][3]*(a[1][0]*(a[2][1]*a[3][2]-a[2][2]*a[3][1])-a[1][1]*(a[2][0]*a[3][2]-a[2][2]*a[3][0])+a[1][2]*(a[2][0]*a[3][1]-a[2][1]*a[3][0]));
        }
        printf("Determinant = %0.2f",d);
    }
    getch();
}